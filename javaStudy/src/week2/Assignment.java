package week2;

public class Assignment {
    public static void main(String[] args) {
        // 2주차 과제
        // 정수형 : byte(8bit), short(16bit), int(32bit), long(64bit), char(8bit)
        // 실수형 : float, double
        // 논리형 : boolean

        // 8개의 데이터 타입에 대해 변수 선언하고 값을 대입해서 출력하는 java 파일 만들기
        byte a = 3;
        System.out.println("byte a의 값 : " + a);
        short b = 5;
        System.out.println("short b의 값: " + b);
        int c = 7;
        System.out.println("int c의 값: " + c);
        long d = 11;
        System.out.println("long d의 값: " + d);
        char e = 'A';
        System.out.println("char e의 값 : " + e);

        float f = 2.35f;
        System.out.println("float f의 값: " + f);
        double g = 3.14;
        System.out.println("double g의 값: " + g);

        boolean h = true;
        System.out.println("boolean h의 값 : " + h);
    }
}
